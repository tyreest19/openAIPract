"""
Calculator library containing basic math operations.
"""
class Calculator():

    def __init__(self):
        pass

    def add(self, first_term, second_term):
        return first_term + second_term

    def subtract(self,first_term, second_term):
        return first_term - second_term

    def divide(self, first_term, second_term):
        return (first_term/second_term)
